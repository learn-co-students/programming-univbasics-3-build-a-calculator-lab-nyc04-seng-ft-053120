def addition(num1, num2)
  add = num1 + num2
  puts add
  add
end

def subtraction(num1, num2)
  minus = num1 - num2
  puts minus
  minus
end

def division(num1, num2)
  divides = num1 / num2
  puts divides
  divides
end

def multiplication(num1, num2)
  multiplies = num1 * num2
  puts multiplies
  multiplies
end

def modulo(num1, num2)
  remainder = num1 % num2
  puts remainder
  remainder
end

def square_root(num)
  square = Integer.sqrt(num)
  puts square
  square
end
